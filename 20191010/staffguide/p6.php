<p>【安全・衛生への具体的な取り組み】</p>
</div>

<div class="main1">

<p class="komi"><span>■</span>あいさつ</p>

<p class="pg">良好な人間関係を構築して、良い職場環境をつくり、仕事をスムーズに回して、安全で健康的に業務をする。<br>これを実現するために、人と人とのコミュニケーションは基本です。教育や情報の伝達・周知など、日々の活動のすべての場面で必要です。<br>コミュニケーションを図る手段としては、まずあいさつです。<br>管理職、正社員、派遣社員など、雇用形態に関わらず 積極的に声を掛け合える職場、風通しのよい職場を作りましょう。</p>

<p class="komi">（1）. あいさつ・返事は元気よく大きな声で</p>
<p class="pg">場所によっては、機械などの稼動音が非常に大きく声が届きません。指揮命令者の方の指示には大きな声で返事をしてください。<br>返事をしていても、先方に聞こえなければ、返事をしていないのと同じことです。<br>また、｢わからない｣ことがあれば、「自分はわかっていない」ということをしっかり伝えないと、労働災害の原因になることがあります。<br>あいさつ・返事のあとは、相手をよく見て、こちらの発言が伝わったことも確認しましょう。</p>

<p class="komi">（2）. 出勤時・退勤時の あいさつ運動</p>
<p class="pg">出勤時、退勤時にあいさつをしてください。好印象はそこから始まります。あいさつであれば、普段あまりコミュニケーションを取らない方にも気軽に声を掛けられます。「おはようございます」「よろしくお願いします」「ありがとうございます」「お疲れさまです」「お先に失礼します」</p>

<p class="komi">（3）. ひとこと発言</p>
<p class="pg">良い点を褒めたり、危険な点を指摘するなど、小さなことでも話しかけるきっかけを見つけて、お互いが声掛けをおこなってください。<br>それにより職場の風通しもよくなります。気になったことを、上長に打ち明けられる環境は、未然に事故を防ぐきっかけになります。</p>

</div><!--/main1-->

<div class="main2">

<p class="komi"><span>■</span>身だしなみ</p>

<p class="pg">職場では正しい ｢身だしなみ｣ を厳守してください。怪我や事故がないように、これまでの経験から生まれているのが、｢職場の身だしなみ｣です。<br>職場ごとに決められた、身だしなみのルールには、必ず従ってください。</p>

<p class="komi">（1）. 安全であるかどうか</p>
<p class="pg">作業現場で靴の紐を固く結ばなければならないのは、つまづくことへの危険性への配慮です。<br>手袋をしなければならないのは、裂傷などの危険性への配慮です。着帽は、頭部打撲の危険性への配慮です。</p>

<p class="komi">（2）. 衛生的であるかどうか</p>
<p class="pg">調理現場で、髪をまとめておく、着用物を清潔にしておく、ひげを剃る、などは、衛生や食中毒への配慮です。</p>

<p class="komi">（3）. 作業能率に影響をあたえないかどうか</p>
<p class="pg">裾（スソ）が伸びているズボンは歩きにくく、作業能率低減の要因になります。</p>

<p class="komi">（4）. 礼儀やサービスに適しているかどうか</p>
<p class="pg">コミュニケーションを周囲と築くのにも、礼儀にかなっているかはとても大事です。またサービス業の場合、アピアランスには常に気を配らなければなりません。職場外では趣味の格好で問題ありませんが、職場においては礼儀を害するほどの個性を発揮することはやめてください。</p>

</div><!--/main2-->

<div class="main3">

<p class="komi"><span>■</span>5S活動</p>

<p class="pg">5S とは、安全衛生活動の基本となる、5つの要素を指します。　5S の徹底を図ると、職場をよく見るようになり、問題点などの顕在化が進みます。<br>このことで、職場の安全性向上のほか、業務の効率化、不具合製品や機密情報の流出を未然に防止 する効果も期待できます。</p>

<div class="main3-left">
<p class="komi">（1）. 業務の一環として活動する</p>
<p class="pg">わざわざ 「5S をしよう」 と思って活動するのではなく、作業前の準備、作業後の片付けの一部などとして、活動しましょう。</p>

<p class="komi">（2）. 全員が活動に参加する</p>
<p class="pg">一人でも活動に参加しない人がいると、5S が徹底できないために活動が進まなかったり、他の人のやる気を削いでしまいます。<br>全員参加で5S を推進しましょう。</p>

<p class="komi">（3）. 各部門・各人の担当する場所を決める</p>
<p class="pg">持ち場を決めておかないと、「あそこは他の人がやる」とみんなが思い、片付けや清掃などが進まない場所がでてきてしまうかもしれません。<br>担当する場所を決めて、各場所を責任を持って管理するようにしましょう。</p>

<p class="komi">（4）. 部門間で定期的に活動状況をチェックし合う</p>
<p class="pg">自分の持ち場を自分で見ているだけでは、不十分な点が見つけられないかもしれません。<br>互いにチェックし合って、良い点を伸ばし、至らない点を改善していきましょう。</p>

</div><!--/main3-left-->

