<p class="komi"><span>■</span>年末調整</p>
<p class="square2">年末調整とは、毎回の給与から源泉徴収される所得税の合計額と、年間の所得について納めなければならない税額との差額を清算することです。</p>

<p class="komi">・年末調整の対象となる方</p>

<p class="sentence">当年中に当社より給与の支払を受け、尚且つ継続して雇用契約がある方が対象となります。対象となる方には年末調整を実施する時期になりましたら別途ご案内を差し上げております。</p>

<p class="sentence">※当年中に当社以外からも給与の支払を受け退職した場合は、その前職分の源泉徴収票が必要です。<br>
※年末調整の対象とならない方は各自確定申告を行っていただく必要があります。確定申告の詳細につきましては税務署の窓口やホームページにてご確認ください。</p>

