<p class="komi">・「給与所得者の扶養控除等（異動）申告書」</p>

<p class="sentence">給与の支払を受ける方は、扶養親族などの有無にかかわらず原則として「給与所得者の扶養控除等（異動）申告書」を提出する必要があります。</p>

<p class="sentence">※一人が同時期に2ヶ所以上の給与支払者に提出することはできません。2ヶ所以上から給与の支払を受けている場合は主たる給与支払者に提出してください。<br>
※毎年最初に給与の支払を受ける日の前日までに提出する必要があります。提出が無い場合は「乙欄」適用になります。<br>
※申告内容（住所・扶養親族等）に変更があった場合は速やかに再提出しなければなりません。</p>

