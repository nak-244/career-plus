 </div>
 <div class="waku2">
 <p class="blue">原因</p>
 <p>パレット台車の旋回する車輪に十分な注意を払わなかったこと。</p>
 </div>
 <div class="waku">
<p class="blue">対策</p>
<p>パレット台車の移動の際は、できるだけ安全靴を使用すること。また、方向を転換する際は、身体の正面と台車の中心がズレた状態で、パレット台車を動かさないこと。重い物はスピードが後から乗るのでゆっくり運ぶこと。</p>
