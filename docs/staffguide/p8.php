 <p class="blue">状況</p>
 <p>入居者の個室にて、入居者を車いすからベッドに移乗させるため、一人の介護職員が入居者の両足を持ち上げ、もう一人の介護職員が入居者の脇を抱えようとしたところ、不自然な動作で入居者を持ち上げようとしたため、両足を持ち上げた介護職員が腰を痛めた。</p>
 </div>
 <div class="waku2">
 <p class="blue">原因</p>
 <p>被介助者に十分身体を近づけず、不自然な姿勢で介護作業を開始したこと。</p>
 </div>
 <div class="waku">
<p class="blue">対策</p>
<p>車椅子からベッドまでの移乗の際は、車椅子と介護ベッドの高さを合わせ、被介助者をできるだけ持ち上げず、横に滑らせること。</p>
</div><!--/ml-bottom-->

