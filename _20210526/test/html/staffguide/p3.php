<p class="komi"><span>■</span>有給休暇</p>
<p class="square2">年次有給休暇は、初回勤務日から6ヶ月継続して勤務した時点で、その間の勤務日数に応じ、7ヶ月目から所定の日数が付与されます。また その後は継続勤務1年ごとに、その1年間の勤務日数に応じて所定の日数が付与されます。（付与日以前1年間（初回付与の場合は6ヶ月間）の出勤率が8割未満の場合には付与されませんのでご注意ください。）<br>ただし雇用契約が結ばれていない期間（勤務していない期間）が1ヶ月に達した場合は、継続勤務とはなりませんので、その後勤務復帰した時点から勤務日数、勤続年数ともに改めて計算されます。</p>

<p class="komi">・利用できない日</p>
<p class="sentence">就業現場の休日、ローテーション勤務制の場合で労働日になっていない日など、あらかじめ休日になっている日には利用できません。</p>

<p class="komi">・申請方法</p>
<p class="sentence">有給休暇は、行使したい日の1週間前までに、「年次有給休暇届」を記載のうえ申請してください。</p>

<p class="komi">・申請の締め切り</p>
<p class="sentence">原則として、行使したい日の1週間前までに、「年次有給休暇届」を記載のうえ申請してください。<br>※ 欠勤扱いとされた日について、後日これを有給休暇に振り替えることはできません。</p>

<p class="komi">・申請単位</p>
<p class="sentence">年次有給休暇の最低単位は1日とし、半日単位・時間単位での請求はできません。</p>

<p class="komi">・時季の変更</p>
<p class="sentence">申請のあった時季に年次有給休暇を利用することが、業務の正常な遂行の妨げになる場合には、その時季を変更していただくことがあります。</p>

