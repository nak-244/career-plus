<p>【安全・衛生の取り組みに対するチェック】</p>
</div>

<div class="main1">

<p class="komi"><span>■</span>安全点検</p>

<p class="pg">私たち労働者は人間です。<br>「ついうっかり」や「急いでいて」、作業手順を誤ったり、不安全な行動をしてしまうことがあります。あるいは、5S が行き届かずに工具・道具・ゴミが散らかっている職場があるかもしれません。<br>そこで、安全点検を行って、私たちの行動、職場の環境を確認し、問題のある点はその場で指摘をして改善を進めましょう。ここでは、安全点検を行う上でのポイントを紹介します。さまざまな方法が考えられますが、知識のある人の目に加えて、経験の浅い人や点検する職場に詳しくない人など、新鮮な目で確認することも有効な手段です。</p>

<p class="komi">（1）. 安全点検において注意すべき項目</p>

<div class="slide">
<p class="komi">①危険な行動を取っている人はいないか</p>
<p class="pg">一般事項（基本的な決まり）、作業手順を守っているか。電気設備、高所作業、回転体、高温物、有機溶剤 など、危険なものを無視・軽視している人がいないか確認しましょう。</p>

<p class="komi">②身だしなみの正しくない人はいないか</p>
<p class="pg">保護服、安全ヘルメット、防護用各種マスクなど正しく装着していないと、 いざというときに大きな事故に繋がります。また、食品白衣、衛生帽子など正しく装着していないと、生産物の食中毒にもつながりかねません。身だしなみの不徹底がないか確認しましょう。</p>

<p class="komi">③事業場内の設備・装置は安全な状態か</p>
<p class="pg">事業場内の床・壁・扉・階段などに危険な箇所がないか、設備・装置を使用する人や周辺の人に危険が及ぶ可能性がないか、暑さ・寒さをはじめとした環境に問題がないかなどを確認しましょう。 空気清浄、採光、防火(消化器の位置確認)など。</p>

<p class="komi">④5Sは徹底できているか</p>
<p class="pg">ゴミが散乱している、工具が床に置いてある、装置が汚れているなど、5S が不十分な点がないか、それにより危険な箇所がないか確認しましょう。廃棄物の対応ルール確認。</p>
</div><!--/slide-->

<p class="komi">（2）. 安全点検の指摘と情報共有</p>

<div class="slide">
<p class="komi">①些細なことも指摘する</p>
<p class="pg">「あのくらいならいいか」ではいけません。小さくても問題点があればすぐに指摘しましょう。 </p>

<p class="komi">②問題を見つけたその場で指摘する </p>
<p class="pg">危険な箇所を見つけたら、原則としてその場で指摘し、改善を促しましょう。<br>
<span class="red">※もし、その場で言いにくければ、支店の派遣元責任者に速やかにお伝えください。</span></p>

<p class="komi">③仲間とともに改善策を検討</p>
<p class="pg">ただ 「ダメだ」 というだけでは、皆のやる気を損ねるだけです。仲間や上司とともに、改善策を検討し、協力して改善を進めましょう。<br>
<span class="red">※もし、その場で言いにくければ、支店の派遣元責任者に速やかにお伝えください。</span></p>

<p class="komi">④指摘事項を職場内で情報共有</p>
<p class="pg">情報を共有して、危険を未然に防止しましょう。他の職場でも作業・環境が類似することは少なくありません。<br>他の職場に同様の危険が無いかを確認し、問題があれば改善を進めましょう。</p>
</div><!--/slide-->

</div><!--/main1-->

<div class="main2">

<p class="komi"><span>■</span>改善提案</p>

<p class="pg">管理者だけでは、現場を十分に知ることができません。皆さん1人1人が実際に働いているスタッフの立場から、どのような環境で仕事をし、どのような危険が日々の作業にあるのか、情報を伝える必要があります。</p>

<p class="komi">（1）. ヒヤリ・ハット報告</p>
<p class="pg">事故や災害には至らなかったが、ヒヤッとしたり、ハッとした出来事を報告し、事故や災害の発生を防止するために 活用しましょう。<br>個人の不注意で片づけないことが大事です。<br>
<span class="red">※もし、その場で言いにくければ、支店の派遣元責任者に速やかにご報告ください。</span></p>

<p class="komi">（2）. 具体的な改善内容を考える</p>
<p class="pg">実際には職場の改善提案は、派遣先の社員の方がされる場合が多いです。しかし、派遣スタッフである皆さんは、 積極的に意見を言い、今後このようなことが起きないようにするためにどうすればよいのかを、一緒に考えるように してください。特にヒヤリ・ハット体験をした方、類似のヒヤリ・ハット体験をされた方は、その体験を無駄にしないよう、 情報共有にも努めてください。<br>他の人が事故を起こさないようにする 「思いやりの心」 がとても大事です。</p>

</div><!--/main2-->

