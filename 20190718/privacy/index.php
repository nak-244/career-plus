<li>当社は最新のＩＴ技術の動向、社会的要請の変化、経営環境の変動等を常に意識しながら、教育、運用、監査などを通じて、適時・適切に個人情報保護マネジメントシステムを見直し、その改善を継続的に行います。</li>
<li>当社では、個人情報の利用目的の通知、開示、訂正、追加又は削除、利用又は第三者への提供の拒否要求、その他苦情、ご相談など、個人情報に関するお問合せについての窓口を設置し、2週間以内に対応いたします。</li>
